// Write a program that will print the remainder from the division of the number 19 with 2, 3, 5 and 8.

#include <stdio.h>

int main() {
    printf("19 / 2 has a remainder of %d\n", 19 % 2);
    printf("19 / 3 has a remainder of %d\n", 19 % 3);
    printf("19 / 5 has a remainder of %d\n", 19 % 5);
    printf("19 / 8 has a remainder of %d", 19 % 8);

    return 0;
}